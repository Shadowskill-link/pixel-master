                        lifespan,<br> ensure the longer
                        comfort and hygienically your boots do not contribute to health problems.</p>
                    <ul>
                        <li>Avoid scuffing the upper leather or treading on sharp edges that could tear or cut the boots
                        </li>
                        <li>Never force dry your boots when they become wet, as direct heat may cause the leather to
                            crack</li>
                        <li>If your boots have a leather upper apply a good wax polish or boot cream regularly</li>
                        <li>Outsoles should be checked regularly and stones and metal shavings should be removed from
                            the cleats</li>
                        <li>If laces become worn, replace immediately to ensure comfort and avoid further damage to boot
